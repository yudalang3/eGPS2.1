package module.heatmap.eheatmap.tree;

public class RadialObjct {

	double xInner;
	double yInner;
	double xOutter;
	double yOutter;
	//Not in radius
	double degree;
	public double getxInner() {
		return xInner;
	}
	public void setxInner(double xInner) {
		this.xInner = xInner;
	}
	public double getyInner() {
		return yInner;
	}
	public void setyInner(double yInner) {
		this.yInner = yInner;
	}
	public double getxOutter() {
		return xOutter;
	}
	public void setxOutter(double xOutter) {
		this.xOutter = xOutter;
	}
	public double getyOutter() {
		return yOutter;
	}
	public void setyOutter(double yOutter) {
		this.yOutter = yOutter;
	}
	public double getDegree() {
		return degree;
	}
	public void setDegree(double degree) {
		this.degree = degree;
	}
	
	
}
