package module.maplot.maplot.model;

import java.awt.geom.Line2D;

public class IndicatingLine {

	private Line2D.Double frontLine;
	private Line2D.Double behindLine;
	public Line2D.Double getFrontLine() {
		return frontLine;
	}
	public void setFrontLine(Line2D.Double frontLine) {
		this.frontLine = frontLine;
	}
	public Line2D.Double getBehindLine() {
		return behindLine;
	}
	public void setBehindLine(Line2D.Double behindLine) {
		this.behindLine = behindLine;
	}
	
	
	
}
